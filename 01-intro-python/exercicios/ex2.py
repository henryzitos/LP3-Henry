# Escreva um programa em Python que solicita ao usuário três números e apresenta a média artimética dos números informados.

n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))
n3 = float(input("Digite o terceiro número: "))

m = (n1 + n2 + n3) / 3

print(m)